Age = 18

if Age < 2:
  print ("You are a baby.")
elif Age >= 2 and Age < 4:
  print ("You are a toddler.")
elif Age >= 4 and Age < 13:
  print ("You are a kid.")
elif Age >= 13 and Age < 20:
  print ("You are a teenager.")
elif Age >= 20 and Age < 65:
  print ("You are an adult.")
else:
  print ("You are an adult.")

  